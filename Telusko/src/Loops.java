
public class Loops {

	public static void main(String[] args) 
	{
		// While Loop 
		
		int i=1;
		while(i<10)
		{
			System.out.println(i);
			i++;
		}
		
		//Do-While loop
		
		int j=1;
		do
		{
			System.out.println(j);
			j++;
		}
		while(j<10);
		
		//For Loop
		for(int k=1;k<10;k++)
		{
			System.out.println(k);
		}
	}

}
