package Arrays;

public class JaggedArrays {

	public static void main(String[] args) 
	{
		// Jagged Arrays are the collection of arrays of different sizes 
		// eg : 5 6 7 8 
		//		6 7
		//		9 8 7
		
		//defining a jagged array
		int p[][]=new int[3][];// leave cols blank as cols are variable
		p[0]=new int[4];
		p[1]=new int[2];
		p[2]=new int[3];
		
		
		
	}

}
